using System;


namespace MyFirstClass
{
    public class MyFirstMath
    {
        public int sum(int x , int y )
        {
            return (x + y);
        }
        public int sum(int x, int y, int z)
        {
            return (x + y + z);
        }
        public short Divide(short x, short y)
        {
            return (short)(x / y);
        }
    }
}
